package servlets.domain;

public class Board {
	
	int contentNo;
	
	String contentId;
	
	String contentPassword;
	
	String contentContent;
	
	String contentImg;
	
	String contentAvi;
	
	int contentLike;
	
	int contentUnlike;
	
	String ContentDate;
	
	String ContentIp;
	
	

	public int getContentNo() {
		return contentNo;
	}

	public void setContentNo(int contentNo) {
		this.contentNo = contentNo;
	}

	public String getContentId() {
		return contentId;
	}

	public void setContentId(String contentId) {
		this.contentId = contentId;
	}

	public String getContentPassword() {
		return contentPassword;
	}

	public void setContentPassword(String contentPassword) {
		this.contentPassword = contentPassword;
	}

	public String getContentContent() {
		return contentContent;
	}

	public void setContentContent(String contentContent) {
		this.contentContent = contentContent;
	}

	public String getContentImg() {
		return contentImg;
	}

	public void setContentImg(String contentImg) {
		this.contentImg = contentImg;
	}

	public String getContentAvi() {
		return contentAvi;
	}

	public void setContentAvi(String contentAvi) {
		this.contentAvi = contentAvi;
	}

	public int getContentLike() {
		return contentLike;
	}

	public void setContentLike(int contentLike) {
		this.contentLike = contentLike;
	}

	public int getContentUnlike() {
		return contentUnlike;
	}

	public void setContentUnlike(int contentUnlike) {
		this.contentUnlike = contentUnlike;
	}

	public String getContentDate() {
		return ContentDate;
	}

	public void setContentDate(String contentDate) {
		ContentDate = contentDate;
	}

	public String getContentIp() {
		return ContentIp;
	}

	public void setContentIp(String contentIp) {
		ContentIp = contentIp;
	}
	
	
	

}
